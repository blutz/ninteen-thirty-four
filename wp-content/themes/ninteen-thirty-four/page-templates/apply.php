    <h4>Frequenty Asked Questions</h4>
    <h5>Who is eligible to apply?</h5>
    <p>To volunteer with UCLA UniCamp, you must be at least 18 years old by June 18, 2019. In addition, you must be either a UCLA student or former UniCamp volunteer/camper (former campers must have finished high school by January 1, 2019).</p>
    <h5>What is the time commitment?</h5>
    <p>During the school year: roughly 2 hours per week plus two all-day weekend events. Events are not held during finals week. You will attend your one week of camp during summer.</p>
    <h5>What if I am unsure of what session I’m most interested in?</h5>
    <p>In the application, you will rate each session based on your interest in the initiative and your availability for training and summer.</p>
    <h5>What if I am unsure of my summer plans?</h5>
    <p>Don’t worry! It’s early on in the school year and we know you may still be thinking about internships, jobs, or summer school. Many internships can work with you, especially for the first few sessions of the summer. Just write a note on your application about your summer plans and we can work with you.</p>
    <h5>Do I need experience as a camp counselor?</h5>
    <p>Nope! We provide all the training needed, based on American Camp Association standards. Depending on the roles you select, you can also learn about lifeguarding, archery, mountain biking, belaying, and more. You will also learn from professionals about topics specific to your session’s population. At camp, you will work in teams others close by if you need help.</p>
    <h5>Will I have help fundraising?</h5>
    <p>Yes! During training, you'll learn how to fundraise. There are also some organized events on- and off-campus that can help you meet your fundraising goal.</p>
